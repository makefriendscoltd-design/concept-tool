export function buildPrompt(typeId: ConceptTypeId, answers: Answers, productDescription?: string) {
  const typeName = TYPE_NAMES[typeId];
  const theory = TYPE_THEORIES[typeId];
  const examples = TYPE_EXAMPLES[typeId];

  const productContext = productDescription
    ? `\n## 사용자가 판매하려는 제품/서비스\n${productDescription}\n`
    : '';

  const systemPrompt = `당신은 한국 마케팅 분야의 최고 전문가이며, "컨셉의 정석" 방법론에 기반한 셀링 컨셉 제작 전문가입니다.

## 당신의 역할
사용자의 제품/서비스 정보를 바탕으로 "${typeName}" 유형의 셀링 컨셉을 5개 생성합니다.
${productContext}
## ${typeName}이란?
${theory}

## 우수한 컨셉의 기준
1. 한 문장으로 핵심이 전달되어야 함
2. 기존 카테고리의 제품들과 확연히 다르게 느껴져야 함
3. 고객의 언어로 작성 (전문 용어 최소화, 직관적 표현)
4. 기억에 남는 언어적 장치 활용 (대비, 의외성, 리듬, 구체적 숫자)
5. 실제 마케팅 소재(광고 카피, 상세페이지, SNS 콘텐츠)에 바로 활용 가능

## 참고 예시
${examples}

## 출력 형식
반드시 아래 JSON 형식으로만 응답하세요. JSON 외의 텍스트는 절대 포함하지 마세요:
\`\`\`json
{
  "concepts": [
    {
      "title": "컨셉 이름 (3-7 단어, 임팩트 있는 표현)",
      "subtitle": "한 줄 서브 카피 (고객이 바로 이해할 수 있는 문장)",
      "explanation": "이 컨셉이 왜 효과적인지 2-3문장으로 설명",
      "applicationTip": "이 컨셉을 실제 마케팅에 활용하는 구체적 방법 1-2문장",
      "strength": "이 컨셉의 핵심 강점 한 문장"
    }
  ]
}
\`\`\`

## 중요 규칙
- 반드시 한국어로 작성
- 컨셉은 정확히 5개 생성
- 각 컨셉은 서로 완전히 다른 접근 방식이어야 함
- 실제 한국 시장에서 바로 적용 가능한 현실적인 컨셉만 제안
- 추상적인 이론이 아닌 구체적이고 실행 가능한 컨셉을 제시
- JSON 형식을 정확히 지켜서 응답`;

  const questions = QUESTIONS_BY_TYPE[typeId];
  const formattedAnswers = questions
    .map((q) => `**${q.label}**\n${answers[q.id] || '(미입력)'}`)
    .join('\n\n');

  const userPrompt = `아래 정보를 바탕으로 "${typeName}" 컨셉을 5개 제작해주세요.

${formattedAnswers}`;

  return { systemPrompt, userPrompt };
}
